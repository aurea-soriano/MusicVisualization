package structures;

/**
 *
 * @author aurea
 */
public class Connection {
    String x;
    String y;

    public Connection(String x, String y) {
        this.x = x;
        this.y = y;
    }

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public String getY() {
        return y;
    }

    public void setY(String y) {
        this.y = y;
    }
    
}
